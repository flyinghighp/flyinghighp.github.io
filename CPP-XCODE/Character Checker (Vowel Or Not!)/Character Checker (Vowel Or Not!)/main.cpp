#include <iostream>
using namespace std;

int main(){
    char mychar;
    
    cout<<"Enter a character :"<<endl;
    cin>>mychar;
    
    if
        (mychar == 'a' || mychar == 'e' || mychar == 'i' || mychar == 'o' || mychar == 'u'){
            cout<<endl<<"Character is a vowel";
        }
    else{
        cout<<endl<<"Character is not a vowel";
    }
    return 0;
}
